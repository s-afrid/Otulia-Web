/**
 * GET ALL PARTNERS
 */
router.get("/partners", authMiddleware, adminCheck, async (req, res) => {
    try {
        const partners = await User.find({})
            .select("-password")
            .sort({ updatedAt: -1 });

        // Map to format suitable for table
        const formattedPartners = partners.map(p => {
            const userObj = p.toObject();
            const totalSales = userObj.soldHistory ? userObj.soldHistory.reduce((acc, curr) => acc + (curr.amount || 0), 0) : 0;

            // Infer category
            let category = 'General';
            if (userObj.myListings && userObj.myListings.length > 0 && userObj.myListings[0].itemModel) {
                category = userObj.myListings[0].itemModel.replace('Asset', '');
            }

            // Generate secure URLs for verification documents using Cloudinary SDK
            const secureDocs = {};
            if (userObj.verificationDocuments) {
                // Handle Map or Object
                const docs = userObj.verificationDocuments instanceof Map 
                    ? Object.fromEntries(userObj.verificationDocuments) 
                    : userObj.verificationDocuments;

                for (const [key, publicId] of Object.entries(docs)) {
                    secureDocs[key] = cloudinary.url(publicId, { 
                        secure: true, 
                        resource_type: 'image' 
                    });
                }
            }

            const companyInfo = userObj.company || null;
            if (companyInfo && companyInfo.companyLogo) {
                companyInfo.companyLogo = cloudinary.url(companyInfo.companyLogo, {
                    secure: true,
                    resource_type: 'image'
                });
            }

            return {
                id: userObj._id,
                name: userObj.name,
                email: userObj.email,
                category: category,
                plan: userObj.plan,
                level: userObj.plan === 'Business VIP' ? 'Platinum' : userObj.plan === 'Premium Basic' ? 'Gold' : 'Silver',
                location: userObj.phone ? 'Verified Loc' : 'Unknown', // Placeholder
                status: userObj.verificationStatus === 'Verified' ? 'Active' : userObj.verificationStatus,
                verificationStatus: userObj.verificationStatus,
                verificationDocuments: secureDocs,
                company: companyInfo,
                totalSales: totalSales,
                isVerified: userObj.isVerified
            };
        });

        res.json(formattedPartners);
    } catch (err) {
        res.status(500).json({ error: "FETCH_PARTNERS_FAILED" });
    }
});

/**
 * VERIFY PARTNER
 */
router.post("/verify-partner", authMiddleware, adminCheck, async (req, res) => {
    try {
        const { userId, action } = req.body; // action: 'approve' | 'reject'

        if (!userId || !action) return res.status(400).json({ error: "MISSING_FIELDS" });

        const user = await User.findById(userId);
        if (!user) return res.status(404).json({ error: "USER_NOT_FOUND" });

        const updateData = {};
        if (action === 'approve') {
            updateData.isVerified = true;
            updateData.verificationStatus = 'Verified';

            // Upgrade role to agent ONLY if they are a standard user. Do not downgrade admins.
            if (user.role === 'user') {
                updateData.role = 'agent';
            }
        } else if (action === 'reject') {
            updateData.isVerified = false;
            updateData.verificationStatus = 'Rejected';
        }

        const updatedUser = await User.findByIdAndUpdate(userId, updateData, { new: true });

        // Create Notification for User
        const notificationMsg = action === 'approve' 
            ? "Congratulations! Your dealer verification has been approved. You can now list assets." 
            : "Your dealer verification was unfortunately rejected. Please check your settings to re-submit documents.";
        
        await User.findByIdAndUpdate(userId, {
            $push: {
                notifications: {
                    type: "VERIFICATION",
                    message: notificationMsg,
                    targetTab: action === 'approve' ? 'inventory' : 'settings',
                    createdAt: new Date()
                }
            }
        });

        // Send Email to User
        const inventoryLink = `${process.env.CLIENT_URL || 'http://localhost:5173'}/inventory`;
        const settingsLink = `${process.env.CLIENT_URL || 'http://localhost:5173'}/inventory`; // Settings is a tab in inventory
        
        const mailOptions = {
            from: process.env.EMAIL_USER,
            to: user.email,
            subject: `Dealer Verification ${action === 'approve' ? 'Approved' : 'Rejected'} - Otulia`,
            text: `Hi ${user.name},

${notificationMsg}

${action === 'approve' 
    ? `Get started by listing your first asset: ${inventoryLink}` 
    : `Review our requirements and re-submit: ${settingsLink}`
}

Best regards,
The Otulia Team`,
        };

        if (process.env.EMAIL_USER && process.env.EMAIL_PASS) {
            transporter.sendMail(mailOptions, (error, info) => {
                if (error) console.error("Verification Email Error:", error);
            });
        }

        // Side Effect: Update Listings Status
        if (user.myListings && user.myListings.length > 0) {
            for (const listing of user.myListings) {
                let Model = null;
                if (listing.itemModel === 'CarAsset') Model = CarAsset;
                else if (listing.itemModel === 'EstateAsset') Model = EstateAsset;
                else if (listing.itemModel === 'BikeAsset') Model = BikeAsset;
                else if (listing.itemModel === 'YachtAsset') Model = YachtAsset;
                else if (listing.itemModel === 'Listing') Model = Listing;

                if (Model) {
                    if (action === 'reject') {
                        // Force hide listings if rejected
                        await Model.findByIdAndUpdate(listing.item, { status: 'Draft' });
                    } else if (action === 'approve') {
                        // Restore listings to Active if approved
                        await Model.findByIdAndUpdate(listing.item, { status: 'Active' });
                    }
                }
            }
        }

        res.json({ message: "SUCCESS", user: updatedUser });
    } catch (err) {
        console.error(err);
        res.status(500).json({ error: "VERIFICATION_ACTION_FAILED" });
    }
});

